        const kofiBtnFooter = document.createElement('a');
        kofiBtnFooter.href = 'https://ko-fi.com/T3F21ZTTDF';
        kofiBtnFooter.target = '_blank';
        kofiBtnFooter.title = 'Support me on Ko-fi';
        kofiBtnFooter.className = 'kofi-btn-footer';
